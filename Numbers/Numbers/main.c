#include <stdio.h>
#include <math.h>



int main(int argc, const char * argv[])
{
    double y = 12345.6789;
    double x = sin(1);
    printf("y is %.2f\n", y);
    printf("y is %e.2\n", y);
    printf("x is %.3f\n", x);
    printf("x is %e.3\n", x);
    return 0;
}

